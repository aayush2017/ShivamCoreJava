
public class Switch {
	public static void main(String s[]) {
		int a = 10;
		switch(a) {
			case 5:
				System.out.println("5/");
			break;
			case 8:
				System.out.println("8");
			break;
			case 10:
				System.out.println("Shivam");
			break;
			default:
				System.out.println("No option Selected");
				break;
		}
	}
}
